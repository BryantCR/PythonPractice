x = 1
y = 2

print(f"La suma de {x} + {y} es: {x + y}")
print("La resta de {} - {} es: {}".format(x, y, x - y))
print(f"La multiplicación de {x} * {y} es: {x * y}")
print("La división de {} / {} es: {}".format(x, y, x / y))
print("La división 'al piso' de {} / {} es: {}".format(x, y, x // y))
print(f"La división 'al modulo' de {x} / {y} es: {x % y}")
print(f"{y} elevado a la {y} es: {y**y}")
print(f"{y} elevado a la {3} es: {y**3}")
print(f"la raíz cuadrada de {y} es: {y**0.5}")




